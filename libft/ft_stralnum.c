#include "libft.h"

int	ft_stralnum(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if ((c[i] < 'a' && c[i] > 'z') || (c[i] < 'A' && c[i] > 'Z')
			|| (c[i] < '0' && c[i] > '9'))
			return (1);
		i++;
	}
	return (0);
}
